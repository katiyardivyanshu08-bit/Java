
public class A_2_char {
   public static void main(String[] args) {
    char c = 'a';
    char b=c;
    System.out.println((int)c); // explicit  (ascii)
    System.out.println(b);

    char ch = 'A';
    System.out.println(ch);   // A
    System.out.println(ch+0);  // 65
    System.out.println(ch*ch);  // 65*65

    } 
}
